/**
 * Generate occasion-specific SEO data
 */
export function generateOccasionSEO(occasionSlug: string, location?: string): OccasionSEO {
  const occasion = OCCASION_SEO_DATA.find(o => o.slug === occasionSlug);
  
  if (!occasion) {
    return {
      name: 'Gifts',
      slug: 'gifts',
      title: 'Kerala Gifts Online - Gift Delivery Service',
      description: 'Premium gift delivery service across Kerala with authentic traditional products.',
      keywords: ['kerala gifts', 'gift delivery', 'traditional products']
    };
  }

  const locationKeywords = location ? [
    `${occasion.slug} gift delivery ${location}`,
    `${occasion.slug} gifts ${location}`,
    `send ${occasion.slug} gifts to ${location}`
  ] : [];

  return {
    ...occasion,
    keywords: [...occasion.keywords, ...locationKeywords],
    structuredData: {
      "@context": "https://schema.org",
      "@type": "Product",
      "name": `${occasion.name} Gifts Kerala`,
      "description": occasion.description,
      "category": "Gifts",
      "offers": {
        "@type": "Offer",
        "availability": "https://schema.org/InStock",
        "priceCurrency": "INR",
        "itemCondition": "https://schema.org/NewCondition"
      },
      "brand": {
        "@type": "Brand",
        "name": "KeralGiftsOnline"
      }
    }
  };
}

/**
 * Get all occasion keywords for homepage
 */
export function getAllOccasionKeywords(): string[] {
  return OCCASION_SEO_DATA.flatMap(occasion => occasion.keywords);
}

